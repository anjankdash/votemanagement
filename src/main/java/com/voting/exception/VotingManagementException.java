package com.voting.exception;


/**
 * @author anjandash@gmail.com
 *
 */
public class VotingManagementException extends Exception {

	public VotingManagementException() {
		super();
		
	}

	public VotingManagementException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public VotingManagementException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public VotingManagementException(String message) {
		super(message);
		
	}

	public VotingManagementException(Throwable cause) {
		super(cause);
		
	}

	 
}
