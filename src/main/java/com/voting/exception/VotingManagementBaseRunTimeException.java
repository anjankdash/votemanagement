package com.voting.exception;


/**
 * @author anjandash@gmail.com
 *
 */
public class VotingManagementBaseRunTimeException extends RuntimeException {

	public VotingManagementBaseRunTimeException() {
		super();
		
	}

	public VotingManagementBaseRunTimeException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public VotingManagementBaseRunTimeException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public VotingManagementBaseRunTimeException(String message) {
		super(message);
		
	}

	public VotingManagementBaseRunTimeException(Throwable cause) {
		super(cause);
		
	}

	 
}
